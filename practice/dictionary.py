#Creating Dictionary and accessing values

dict ={'Name':'Zara','Age':7,'class':'first'}

print("dict['Name']:",dict['Name'])
print("dict['Age']:",dict['Age'])

#updating dictionary

dict['Age']=8;
dict['School']="DPS School"

print ("dict['Age']:",dict['Age'])
print ("dict['School']",dict['School'])

#deleting dictionary elements

del dict['Name']

print(dict)


