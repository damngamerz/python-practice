#Creating a tuple

tup1 = ('physics', 'chemistry',1997,2000)
tup2 = (1,2,3,4,5,6,7)
tup3 = "a","b","c","d"

#Accessing Values of Tuples

print("tup1[0]:",tup1[0])
print("tup2[1:5]",tup2[1:5])

#updating Tuples

tup4=tup2+tup3

print(tup4)


#Deleting Tuple Elements

del tup4;
print("tup4 deleted")

